// Array Methods
let num = [1, 2, 3, 34, 4]
let b = num.toString() // b is now a string 
console.log(b, typeof b)

let c = num.join(" and ")
console.log(c, typeof c)

let r = num.pop() // The pop() method removes (pops) the last element of an array.
console.log(num, r) // The pop() method changes the original array and The pop() method returns the removed element.

